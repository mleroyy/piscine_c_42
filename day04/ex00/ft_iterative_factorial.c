int		ft_iterative_factorial(int nb)
{
	int		i;

	i = nb;
	if (nb == 0)
		return (1);
	if (nb >= 1 && nb < 13)
	{
		while (i >= 2)
		{
			nb = nb * (i - 1);
			i--;
		}
		return (nb);
	}
	else
		return (0);
}
